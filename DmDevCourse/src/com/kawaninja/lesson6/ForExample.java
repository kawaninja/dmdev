package com.kawaninja.lesson6;

public class ForExample {
	public static void main(String[] args) {
		for (int i =10; i > 0; i--) {
			
			if (i == 5) {
				continue;
//				break;
			}
			System.out.println(i);
		}
		}
		public static void forExample() {
			for (int value = 10; value >0; value -- ) {
				System.out.println(value);
			}
}

}
