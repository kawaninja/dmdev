package com.kawaninja.lesson6;

public class WhileExample {
	public static void main(String[] args) {
		int value = 0;
		
		do {
			value ++;
			System.out.println(value);
			
		} while (value <10);
		
	}
		private static void whileExample () {
		int value = 10;
		while (value > 0) {
			System.out.println(value + " >0");
			value --;
		}
		
		}
}
	


